import numpy as np

def check_line_of_sight(image, x1, y1, x2, y2, thresh=200):
    line = get_line(x1,y1,x2,y2)
    # check for collisions
    for point in line:
        value = image[point[1],point[0]] # not sure why the points are flipped, but it works
        if value < thresh: # colliding
            print("collision at:",point,"--value",value)
            return False
    return True



def get_line(x1, y1, x2, y2):
    points = []
    issteep = abs(y2-y1) > abs(x2-x1)
    if issteep:
        x1, y1 = y1, x1
        x2, y2 = y2, x2
    rev = False
    if x1 > x2:
        x1, x2 = x2, x1
        y1, y2 = y2, y1
        rev = True
    deltax = x2 - x1
    deltay = abs(y2-y1)
    error = int(deltax / 2)
    y = y1
    ystep = None
    if y1 < y2:
        ystep = 1
    else:
        ystep = -1
    for x in range(x1, x2 + 1):
        if issteep:
            points.append((y, x))
        else:
            points.append((x, y))
        error -= deltay
        if error < 0:
            y += ystep
            error += deltax
    # Reverse the list if the coordinates were reversed
    if rev:
        points.reverse()
    return points